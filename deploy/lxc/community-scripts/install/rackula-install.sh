source /dev/stdin <<<"$FUNCTIONS_FILE_PATH"
color
verb_ip6
catch_errors
setting_up_container
network_check
update_os

msg_info "Installing Dependencies"
$STD apt install -y \
  nginx \
  curl
msg_ok "Installed Dependencies"

msg_info "Creating rackula user"
if ! id -u rackula >/dev/null 2>&1; then
  useradd --system --user-group --home-dir /opt/rackula --shell /usr/sbin/nologin rackula
fi
msg_ok "Created rackula user"

msg_info "Installing Rackula"
fetch_and_deploy_gh_release "rackula" "RackulaLives/Rackula" "prebuild" "latest" "/opt/rackula" "rackula-lxc-*.tar.gz"

# Integrity verification: re-download the tarball and verify its SHA256 against
# the published checksum. fetch_and_deploy_gh_release extracted and removed the
# tarball, so we download it again solely for verification. The tarball is small
# (a few MB), acceptable for a one-time install.
_INSTALLED_VER="$(cat ~/.rackula 2>/dev/null)"
if [[ -n "$_INSTALLED_VER" ]]; then
  _INSTALLED_TAG="v${_INSTALLED_VER}"
  _VDIR=$(mktemp -d) || { msg_error "Cannot create temp dir for verification"; exit 1; }
  _TARBALL="rackula-lxc-${_INSTALLED_TAG}.tar.gz"
  _TARBALL_URL="https://github.com/RackulaLives/Rackula/releases/download/${_INSTALLED_TAG}/${_TARBALL}"
  _CHECKSUM_URL="${_TARBALL_URL}.sha256"
  if curl_download "$_VDIR/${_TARBALL}.sha256" "$_CHECKSUM_URL" 2>/dev/null; then
    if curl_download "$_VDIR/$_TARBALL" "$_TARBALL_URL" 2>/dev/null; then
      _EXPECTED=$(awk '{print $1}' "$_VDIR/${_TARBALL}.sha256")
      _ACTUAL=$(sha256sum "$_VDIR/$_TARBALL" | awk '{print $1}')
      if [[ "$_EXPECTED" != "$_ACTUAL" ]]; then
        msg_error "SHA256 verification failed for $_TARBALL"
        msg_error "Expected: ${_EXPECTED}"
        msg_error "Actual:   ${_ACTUAL}"
        rm -rf /opt/rackula "$_VDIR"
        exit 1
      fi
      msg_ok "SHA256 checksum verified"
    else
      msg_error "Failed to re-download tarball for SHA256 verification"
      rm -rf /opt/rackula "$_VDIR"
      exit 1
    fi
  else
    msg_warn "Could not download checksum for verification (release may predate checksums)"
  fi
  rm -rf "$_VDIR"
fi

# Create persistent data directory (not in tarball)
mkdir -p /opt/rackula/data
mkdir -p /etc/nginx/snippets

# Deploy config files from the release
SECURITY_HEADERS_SRC="/opt/rackula/config/security-headers.conf"
if [ ! -f "$SECURITY_HEADERS_SRC" ]; then
  msg_error "Required config file missing: $SECURITY_HEADERS_SRC (release may be incomplete)"
  exit 1
fi
cp "$SECURITY_HEADERS_SRC" /etc/nginx/snippets/security-headers.conf

# Set ownership: frontend root-owned (served by nginx), API rackula-owned
chown -R root:root /opt/rackula/frontend
find /opt/rackula/frontend -type d -exec chmod 755 {} \;
find /opt/rackula/frontend -type f -exec chmod 644 {} \;
chown -R rackula:rackula /opt/rackula/api
chown -R rackula:rackula /opt/rackula/data
chmod 750 /opt/rackula/data
msg_ok "Installed Rackula"

msg_info "Generating API write token"
API_WRITE_TOKEN=$(openssl rand -hex 32)

# CORS origin scheme — defaults to http. Behind an HTTPS reverse proxy, export
# CORS_SCHEME=https before running the installer, or edit CORS_ORIGIN in
# /opt/rackula/data/.env afterwards and restart rackula-api. Only http/https
# are accepted; anything else is rejected to avoid writing a malformed origin.
CORS_SCHEME="${CORS_SCHEME:-http}"
case "$CORS_SCHEME" in
  http | https) ;;
  *)
    msg_error "Invalid CORS_SCHEME '${CORS_SCHEME}' (expected 'http' or 'https')"
    exit 1
    ;;
esac

# Insecure CORS is disabled by default. Set ALLOW_INSECURE_CORS=true explicitly
# before running installer only when required by your deployment.
ALLOW_INSECURE_CORS="${ALLOW_INSECURE_CORS:-false}"
case "$ALLOW_INSECURE_CORS" in
  true | false) ;;
  *)
    msg_error "Invalid ALLOW_INSECURE_CORS '${ALLOW_INSECURE_CORS}' (expected 'true' or 'false')"
    exit 1
    ;;
esac

# Write API environment file
cat <<EOF >/opt/rackula/data/.env
RACKULA_API_WRITE_TOKEN=${API_WRITE_TOKEN}
CORS_ORIGIN=${CORS_SCHEME}://localhost
ALLOW_INSECURE_CORS=${ALLOW_INSECURE_CORS}
EOF
chown rackula:rackula /opt/rackula/data/.env
chmod 600 /opt/rackula/data/.env

# Write nginx token snippet (included by nginx.conf)
cat <<EOF >/etc/nginx/snippets/rackula-api-token.conf
# Auto-generated by rackula-install.sh — do not edit manually
set \$rackula_api_write_token "${API_WRITE_TOKEN}";
set \$rackula_has_api_write_token 1;
EOF
chown root:www-data /etc/nginx/snippets/rackula-api-token.conf
chmod 640 /etc/nginx/snippets/rackula-api-token.conf
msg_ok "Generated API write token"

msg_info "Configuring nginx"

# Deploy rackula nginx site config from the packaged tarball (single source of truth)
cp /opt/rackula/config/nginx.conf /etc/nginx/sites-available/rackula

# Remove default site, enable rackula
rm -f /etc/nginx/sites-enabled/default
ln -sf /etc/nginx/sites-available/rackula /etc/nginx/sites-enabled/rackula
msg_ok "Configured nginx"

msg_info "Creating Services"
# Deploy the hardened API unit from the packaged tarball (single source of truth)
cp /opt/rackula/config/rackula-api.service /etc/systemd/system/rackula-api.service

# Harden nginx service via drop-in override (copy from packaged config)
mkdir -p /etc/systemd/system/nginx.service.d
cp /opt/rackula/config/nginx.service.d-override.conf /etc/systemd/system/nginx.service.d/override.conf

systemctl daemon-reload
systemctl enable -q --now rackula-api
systemctl enable -q --now nginx
msg_ok "Created Services"

msg_info "Verifying Services"
for i in $(seq 0 9); do
  if curl -sf --connect-timeout 2 --max-time 5 http://127.0.0.1:3001/health >/dev/null 2>&1; then
    msg_ok "Service running successfully"
    break
  fi
  sleep 1
  if [ "$i" -eq 9 ]; then
    msg_error "API failed to start within 10 seconds"
    exit 1
  fi
done

motd_ssh
customize
cleanup_lxc